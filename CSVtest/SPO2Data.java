package CSVtest;

public class SPO2Data
{
    private String date;
    private String time;
    private int spo2;
    private int pulse;

    public SPO2Data()
    {
    	//empty constructor
    }
    
    public SPO2Data(String date, String time, int firstReading, int secondReading)
    {
    	this.date = date;
    	this.time = time;
    	this.spo2 = spo2;
    	this.pulse = pulse;
    }
    
    public String getDate()
    {
		return this.date;
	}
	public void setDate(String date)
	{
		this.date = date;
	}
 	public String getTime()
 	{
		return this.time;
	}
	public void setTime(String time)
	{
		this.time = time;
	}
 	public int getSPO2()
 	{
		return this.spo2;
	}
	public void setSPO2(int spo2)
	{
		this.spo2 = spo2;
	}
 	public int getPulse()
 	{
		return this.pulse;
	}
	public void setPulse(int pulse)
	{
		this.pulse = pulse;
	}
}
